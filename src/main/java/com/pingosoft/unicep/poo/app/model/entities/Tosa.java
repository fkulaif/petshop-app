package com.pingosoft.unicep.poo.app.model.entities;

public class Tosa {
	private String tosa;
	private String tosaHigienica;
	private String obs;
	private Double preco;

	public String getTosa() {
		return this.tosa;
	}

	public void setTosa(String tosa) {
		this.tosa = tosa;
	}

	public String getTosaHigienica() {
		return this.tosaHigienica;
	}

	public void setTosaHigienica(String tosaHigienica) {
		this.tosaHigienica = tosaHigienica;
	}

	public String getObs() {
		return this.obs;
	}

	public void setObs(String obs) {
		this.obs = obs;
	}

	public Double getPreco() {
		return this.preco;
	}

	public void setPreco(Double preco) {
		this.preco = preco;
	}

}
